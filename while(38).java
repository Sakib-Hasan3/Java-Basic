/*
while loop syntax
----------------
    initialization;
    while(condition)
    {
        statement(s)
        increment/decrement
    }
*/
public class whileloop
{
    public static void main(String[] args) {
        int i=1;
        while(i<=10)
        {
            System.out.println("Bangladesh");
            i++;
        }
    }
}
