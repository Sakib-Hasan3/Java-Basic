

/*
do while loop syntax
----------------
    initialization;
   do
    {
        statement(s)
        increment/decrement
    }while(condition);
*/
public class dowhileloop 
{
    public static void main(String[] args) 
    {
        int i=1;
        do
        {
            System.out.println("Bangladesh");
            i++;
        }while(i<=10);
    }
}
