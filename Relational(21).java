/* Equal to (==): This operator is used to compare two operands. If both operands are equal, it returns true; otherwise, it returns false.
java */
int a = 10;
int b = 10;
System.out.println(a == b); // Output: true

/* Not equal to (!=): This operator is used to compare two operands. If both operands are not equal, it returns true; otherwise, it returns false.
java */

int a = 10;
int b = 20;
System.out.println(a != b); // Output: true

/* Greater than (>): This operator is used to compare two operands. If the first operand is greater than the second operand, it returns true; otherwise, it returns false.
java */

int a = 20;
int b = 10;
System.out.println(a > b); // Output: true

/* Less than (<): This operator is used to compare two operands. If the first operand is less than the second operand, it returns true; otherwise, it returns false.
java */

int a = 10;
int b = 20;
System.out.println(a < b); // Output: true

/* Greater than or equal to (>=): This operator is used to compare two operands. If the first operand is greater than or equal to the second operand, it returns true; otherwise, it returns false.
java */

int a = 20;
int b = 10;
System.out.println(a >= b); // Output: true

